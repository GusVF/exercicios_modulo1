function add(a, b){
    return a + b;
}
console.log(add(40 , 60));

function subtract(a, b){
    return a - b;
}
console.log(subtract(200, 100));

function multiply(a, b){
    return a * b;
}
console.log(multiply(20, 5));

function divide(a, b){
    return a / b;
}
console.log(divide(1000, 10));

function modulate(a, b){
    return a % b;
}
console.log(modulate(100, 2));